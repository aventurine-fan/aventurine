// Фанатский сайт Авантюрина - Гарантированный выигрыш на 3-й попытке
const СТАРТОВЫЙ_БАЛАНС = 150;
const СТОИМОСТЬ_СПИНА = 50;

let баланс = СТАРТОВЫЙ_БАЛАНС;
let номерПопытки = 0;
let анимация = false;

const балансЭл = document.getElementById('balance');
const кнопка = document.getElementById('spinButton');
const сообщение = document.getElementById('resultMessage');
const счётчикПопыток = document.getElementById('attemptCounter');
const барабан1 = document.getElementById('reel1');
const барабан2 = document.getElementById('reel2');
const барабан3 = document.getElementById('reel3');

const символы = ["💎", "🎲", "⭐", "♣️", "♦️", "🍾"];

function случайныйСимвол() {
    return символы[Math.floor(Math.random() * символы.length)];
}

function обновитьЭкран() {
    балансЭл.innerText = баланс;
    if (номерПопытки === 0) счётчикПопыток.innerText = "🍀 Попытка №1 · Гарантированный выигрыш на 3-й раз 🍀";
    else if (номерПопытки === 1) счётчикПопыток.innerText = "🎲 Попытка №2 · Остался один шаг до джекпота! 🎲";
    else if (номерПопытки === 2) счётчикПопыток.innerText = "💎 ПОСЛЕДНЯЯ ПОПЫТКА · 100% ВЫИГРЫШ! 💎";
    
    if (баланс < СТОИМОСТЬ_СПИНА || номерПопытки >= 3) {
        кнопка.disabled = true;
        if (номерПопытки >= 3) кнопка.innerText = "🏆 ЗАБЕРИ СВОЙ ПРИЗ 🏆";
    } else {
        кнопка.disabled = false;
    }
}

function получитьРезультат() {
    const гарант = (номерПопытки === 2);
    
    if (гарант) {
        return { с1: "💎", с2: "💎", с3: "💎", победа: true };
    } else {
        let с1 = случайныйСимвол();
        let с2 = случайныйСимвол();
        let с3 = случайныйСимвол();
        const джекпот = (с1 === "💎" && с2 === "💎" && с3 === "💎");
        return { с1, с2, с3, победа: джекпот };
    }
}

function крутить() {
    if (анимация) return;
    if (баланс < СТОИМОСТЬ_СПИНА) {
        сообщение.innerHTML = "⚠️ Фишки закончились... Забери свой приз! ⚠️";
        return;
    }
    
    анимация = true;
    кнопка.disabled = true;
    сообщение.innerHTML = "🎲 Барабаны крутятся... Авантюрин дарит удачу! 🎲";
    
    баланс -= СТОИМОСТЬ_СПИНА;
    номерПопытки++;
    обновитьЭкран();
    
    let интервал = setInterval(() => {
        if (!анимация) return;
        барабан1.innerText = случайныйСимвол();
        барабан2.innerText = случайныйСимвол();
        барабан3.innerText = случайныйСимвол();
    }, 70);
    
    setTimeout(() => {
        clearInterval(интервал);
        const финал = получитьРезультат();
        барабан1.innerText = финал.с1;
        барабан2.innerText = финал.с2;
        барабан3.innerText = финал.с3;
        
        if (финал.победа) {
            сообщение.innerHTML = "✨✨✨ ДЖЕКПОТ! ✨✨✨<br>🏆 ВЫ ВЫИГРАЛИ! 🏆<br>🎁 ПРОЙДИТЕ В СОСЕДНЮЮ КОМНАТУ ЗА ПРИЗОМ! 🎁";
            кнопка.disabled = true;
            баланс = 0;
            обновитьЭкран();
            document.querySelector('.casino-wrapper').style.boxShadow = "0 0 60px #ff66cc";
        } else {
            if (номерПопытки === 2) {
                сообщение.innerHTML = "🌸 Последняя попытка точно будет удачной! Крути ещё! 🌸";
            } else {
                сообщение.innerHTML = "🍂 Не в этот раз... Авантюрин говорит: попробуй ещё! 🍂";
            }
        }
        
        анимация = false;
        if (баланс >= СТОИМОСТЬ_СПИНА && номерПопытки < 3) {
            кнопка.disabled = false;
        } else if (номерПопытки === 3) {
            кнопка.disabled = true;
            кнопка.innerText = "🎁 ЗАБЕРИ ФИГУРКУ 🎁";
            сообщение.innerHTML = "🏆💎 ТЫ ВЫИГРАЛА 5000 ЮАНЕЙ! ПРОЙДИ В СОСЕДНЮЮ КОМНАТУ ЗА ФИГУРКОЙ АВАНТЮРИНА + КОНВЕРТ! 💎🏆";
        }
    }, 1200);
}

кнопка.addEventListener('click', крутить);
обновитьЭкран();